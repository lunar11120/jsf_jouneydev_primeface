package com.jou.model;

public class List101 {
	private String id;
	private String name;
	private String bigName;
	
	
	public List101() {
		super();
	}
	public List101(String id, String name, String bigName) {
		super();
		this.id = id;
		this.name = name;
		this.bigName = bigName;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBigName() {
		return bigName;
	}
	public void setBigName(String bigName) {
		this.bigName = bigName;
	}
	

}
